    private void btnRemoverTelefoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverTelefoneActionPerformed
        int linha = tblTelefones.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um telefone para remover.");
            return;
        }

        if (telefonesTemp.size() == 1) {
            JOptionPane.showMessageDialog(this, "Tutor precisa ter pelo menos um telefone.");
            return;
        }

        telefonesTemp.remove(linha);
        atualizarTabelaTelefones(telefonesTemp);
    }//GEN-LAST:event_btnRemoverTelefoneActionPerformed

