    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty() || txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha os campos nome e email");
            return;
        }

        tutorAtual.setNome(txtNome.getText());
        tutorAtual.setEmail(txtEmail.getText());

        if (telefonesTemp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tutor precisa ter telefone");
            return;
        }

        tutorAtual.getTelefones().clear();
        tutorAtual.getTelefones().addAll(telefonesTemp);

        TutorDAO dao = new TutorDAO();

        for (Telefone t : tutorAtual.getTelefones()) {
            t.setTutor(tutorAtual);
        }
        
        if (tutorAtual.getId() == null) {
            tutorAtual = dao.cadastrar(tutorAtual);
            JOptionPane.showMessageDialog(this, "Tutor cadastrado com sucesso!");
        } else {
            dao.atualizar(tutorAtual);
            JOptionPane.showMessageDialog(this, "Tutor atualizado com sucesso!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

