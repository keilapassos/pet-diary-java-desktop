    private void preencherCampos() {
        txtNome.setText(tutorAtual.getNome());
        txtEmail.setText(tutorAtual.getEmail());

        telefonesTemp = new ArrayList<>(tutorAtual.getTelefones());
        atualizarTabelaTelefones(telefonesTemp);
    }

    public void atualizarTabelaTelefones(List<Telefone> lista) {

        telefonesTemp = lista;

        DefaultTableModel model = (DefaultTableModel) tblTelefones.getModel();
        model.setRowCount(0);

        for (Telefone t : lista) {
            model.addRow(new Object[]{
                t.getNumero(),
                t.getTipo()
            });
        }
    }

