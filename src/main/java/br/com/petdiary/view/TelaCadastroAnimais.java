    public TelaCadastroAnimais(Animal animal) {
        initComponents();
        setLocationRelativeTo(null);
        setTitle("Editar Animal");

        if (animal == null) {
            JOptionPane.showMessageDialog(this, "Animal inválido.");
            dispose();
            return;
        }

        this.animalAtual = animal;

        preencherCampos();
    }

    public void receberTutorSelecionado(Tutor tutor) {
        this.tutorSelecionado = tutor;
        lblTutorSelecionado.setText(tutor.getNome());
    }

    private void preencherCampos() {
        txtNome.setText(animalAtual.getNome());
        txtRaca.setText(animalAtual.getRaca());
        txtIdade.setText(String.valueOf(animalAtual.getIdade()));
        txtObservacoes.setText(animalAtual.getObservacoes());
        comboEspecie.setSelectedItem(animalAtual.getEspecie());

        tutorSelecionado = animalAtual.getTutor();
        if (tutorSelecionado != null) {
            lblTutorSelecionado.setText(tutorSelecionado.getNome());
        }
    }

