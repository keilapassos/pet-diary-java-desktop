    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Nome.");
            return;
        }

        if (txtRaca.getText().isEmpty()) {
            txtRaca.setText("");
        }

        if (txtIdade.getText().isEmpty() || !txtIdade.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Idade com números.");
            return;
        }

        if (txtObservacoes.getText().isEmpty()) {
            txtObservacoes.setText("");
        }

        if (tutorSelecionado == null) {
            JOptionPane.showMessageDialog(this, "Selecione um tutor.");
            return;
        }

        animalAtual.setNome(txtNome.getText());
        animalAtual.setEspecie(comboEspecie.getSelectedItem().toString());
        animalAtual.setRaca(txtRaca.getText());
        animalAtual.setIdade(Integer.parseInt(txtIdade.getText()));
        animalAtual.setObservacoes(txtObservacoes.getText());
        animalAtual.setTutor(tutorSelecionado);

        AnimalDAO dao = new AnimalDAO();

        if (animalAtual.getId() == null) {
            animalAtual = dao.cadastrar(animalAtual);
            JOptionPane.showMessageDialog(this, "Animal cadastrado com sucesso!");
        } else {
            dao.atualizar(animalAtual);
            JOptionPane.showMessageDialog(this, "Animal atualizado com sucesso!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

