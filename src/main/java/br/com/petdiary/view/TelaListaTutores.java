    private void tblTutoresCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTutoresCadastradosMouseClicked
        if (modoSelecao) {
            int linha = tblTutoresCadastrados.getSelectedRow();

            if (linha == -1) {
                return;
            }

            Tutor tutorSelecionado = listaAtual.get(linha);

            if (telaOrigem instanceof TelaCadastroAnimais tela) {
                tela.receberTutorSelecionado(tutorSelecionado);
            }

            if (telaOrigem instanceof TelaAtendimentoVeterinario tela) {
                tela.receberTutorSelecionado(tutorSelecionado);
            }
            dispose();
        }
    }//GEN-LAST:event_tblTutoresCadastradosMouseClicked

