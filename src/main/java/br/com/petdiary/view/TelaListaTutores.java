    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tblTutoresCadastrados.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um tutor!");
            return;
        }

        Tutor tutorSelecionado = listaAtual.get(linha);

        TelaCadastroTutores tela = new TelaCadastroTutores(tutorSelecionado);
        tela.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnEditarActionPerformed

