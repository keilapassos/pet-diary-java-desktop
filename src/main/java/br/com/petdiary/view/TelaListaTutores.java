    private void carregarTutores() {
        DefaultTableModel model = (DefaultTableModel) tblTutoresCadastrados.getModel();
        model.setRowCount(0);

        listaAtual.clear();

        TutorDAO dao = new TutorDAO();
        List<Tutor> listaBanco = dao.listar();        

        for (Tutor t : listaBanco) {
            String telefones = "";
            
            for (Telefone tel : t.getTelefones()) {
                if (!telefones.isEmpty()) {
                    telefones += ", ";
                }
                telefones += tel.getNumero();
            }
            listaAtual.add(t);
            model.addRow(new Object[]{
                t.getNome(),
                t.getEmail(),
                telefones
            });
        }
    }

