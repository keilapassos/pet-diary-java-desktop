    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tblTelefones.getModel();
        model.setRowCount(0);

        for (Telefone t : lista) {
            model.addRow(new Object[]{
                t.getNumero(),
                t.getTipo()
            });
        }
    }

