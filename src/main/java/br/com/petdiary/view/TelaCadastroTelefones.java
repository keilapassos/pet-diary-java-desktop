    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String numero = txtTelefone.getText().replaceAll("\\D", "");

        if (numero.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um telefone");
            return;
        }      
        
        if (numero.length() < 10 || numero.length() > 11) {
            JOptionPane.showMessageDialog(this, "Telefone deve ter 10 ou 11 dígitos");
            return;
        }

        lista.add(new Telefone(
                numero,
                comboTipo.getSelectedItem().toString()
        ));

        txtTelefone.setText("");
        atualizarTabela();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Adicione pelo menos um telefone");
            return;
        }

        telaPai.atualizarTabelaTelefones(lista);
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

