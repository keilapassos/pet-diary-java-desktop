    private void carregarAnimais() {
        DefaultTableModel model = (DefaultTableModel) tblAnimais.getModel();
        model.setRowCount(0);

        listaAtual.clear();

        AnimalDAO dao = new AnimalDAO();
        List<Animal> listaBanco = dao.listar();

        for (Animal a : listaBanco) {
            listaAtual.add(a);
            model.addRow(new Object[]{
                a.getNome(),
                a.getEspecie(),
                a.getIdade(),
                a.getTutor().getNome()
            });
        }
    }

