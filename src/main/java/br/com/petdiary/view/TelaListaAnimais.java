    private void txtBuscaNomeAnimalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaNomeAnimalKeyReleased
        String busca = txtBuscaNomeAnimal.getText().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) tblAnimais.getModel();
        model.setRowCount(0);

        listaAtual.clear();

        AnimalDAO dao = new AnimalDAO();
        List<Animal> listaBanco = dao.listar();

        for (Animal a : listaBanco) {
            if (a.getNome().toLowerCase().contains(busca)) {
                listaAtual.add(a);
                model.addRow(new Object[]{
                    a.getNome(),
                    a.getEspecie(),
                    a.getIdade(),
                    a.getTutor().getNome()
                });
            }
        }
    }//GEN-LAST:event_txtBuscaNomeAnimalKeyReleased

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tblAnimais.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um animal!");
            return;
        }

        Animal animalSelecionado = listaAtual.get(linha);

        TelaCadastroAnimais tela = new TelaCadastroAnimais(animalSelecionado);
        tela.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnEditarActionPerformed

